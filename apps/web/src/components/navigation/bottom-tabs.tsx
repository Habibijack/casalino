'use client';

import { usePathname } from 'next/navigation';
import { useTranslations } from 'next-intl';

const tabs = [
  { key: 'search', href: '/dashboard', icon: SearchIcon },
  { key: 'listings', href: '/listings', icon: HeartIcon },
  { key: 'dossier', href: '/dossier', icon: FolderIcon },
  { key: 'profile', href: '/profile', icon: UserIcon },
] as const;

export function BottomTabs({ locale }: { locale: string }) {
  const pathname = usePathname();
  const t = useTranslations('nav');

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-card border-t border-border safe-area-bottom">
      <div className="flex items-center justify-around h-16 max-w-lg mx-auto px-2">
        {tabs.map((tab) => {
          const fullHref = `/${locale}${tab.href}`;
          const isActive =
            pathname === fullHref || pathname.startsWith(`${fullHref}/`);
          const Icon = tab.icon;

          return (
            <a
              key={tab.key}
              href={fullHref}
              className={`flex flex-col items-center justify-center w-full h-full gap-0.5 transition-colors ${
                isActive
                  ? 'text-primary'
                  : 'text-muted-foreground hover:text-foreground'
              }`}
            >
              <Icon className="w-5 h-5" filled={isActive} />
              <span className="text-[10px] font-medium">{t(tab.key)}</span>
            </a>
          );
        })}
      </div>
    </nav>
  );
}

function SearchIcon({
  className,
  filled,
}: {
  className?: string;
  filled?: boolean;
}) {
  return (
    <svg
      className={className}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={filled ? 2.5 : 2}
      strokeLinecap="round"
      strokeLinejoin="round"
    >
      <circle cx="11" cy="11" r="8" />
      <line x1="21" y1="21" x2="16.65" y2="16.65" />
    </svg>
  );
}

